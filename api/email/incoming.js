/**
 * Format a booking record into a context line for the AI
 */
function formatBookingContext(b) {
  const ref = b.referenceNumber || (b.id ? b.id.slice(0, 5) : 'N/A');
  const date = b.date || 'N/A';
  const time = b.time || 'N/A';
  const pickup = b.pickupAddress || 'N/A';
  const dropoff = b.dropoffAddress || 'N/A';
  const status = b.status || 'unknown';
  const payment = b.payment_status || b.paymentStatus || 'unknown';
  const price = b.totalPrice || b.price || 'N/A';
  const passengers = b.passengers || 1;
  const tripType = b.tripType || 'one-way';
  const name = b.name || `${b.firstName || ''} ${b.lastName || ''}`.trim() || 'N/A';
  return `  - Ref: ${ref} | Date: ${date} ${time} | ${pickup} -> ${dropoff} | Status: ${status} | Payment: ${payment} | Price: $${price} | Passengers: ${passengers} | Trip: ${tripType} | Name: ${name}\n`;
}

/**
 * Call Claude API (raw HTTP — no SDK dependency)
 */
async function callClaude(systemPrompt, userMessage) {
  const apiKey = (process.env.ANTHROPIC_API_KEY || '').trim();
  if (!apiKey) return null;

  const res = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': apiKey,
      'anthropic-version': '2023-06-01',
    },
    body: JSON.stringify({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 500,
      system: systemPrompt,
      messages: [{ role: 'user', content: userMessage }],
    }),
  });

  if (!res.ok) {
    const errText = await res.text();
    console.error(`Claude API ${res.status}: ${errText}`);
    return null;
  }

  const data = await res.json();
  return (data.content?.[0]?.text || '').trim();
}

