/**
 * Parse ACTION tags from Claude's response and execute them
 */
async function processActions(aiResponse, senderEmail, senderName, customerBookings) {
  const actionsTaken = [];

  // Build lookup by reference
  const byRef = {};
  for (const b of customerBookings) {
    const ref = b.referenceNumber || '';
    if (ref) {
      byRef[String(ref)] = b;
      byRef[`#${ref}`] = b;
    }
    if (b.id) byRef[b.id.slice(0, 8).toUpperCase()] = b;
  }

  // RESEND_CONFIRMATION
  const resendMatches = aiResponse.matchAll(/\[ACTION:RESEND_CONFIRMATION:([^\]]+)\]/g);
  for (const m of resendMatches) {
    const ref = m[1].trim().replace(/^#/, '');
    const booking = byRef[ref] || byRef[`#${ref}`];
    if (booking) {
      // Trigger resend via the send-booking-email endpoint logic
      try {
        const { sendEmail: send } = require('../_lib/mailgun');
        const { customerBookingConfirmedEmail } = require('../_lib/email-templates');
        const tmpl = customerBookingConfirmedEmail(booking);
        const customerEmail = booking.email || senderEmail;
        await send({ to: customerEmail, subject: tmpl.subject, html: tmpl.html, fromName: 'BookaRide' });
        actionsTaken.push(`Resent confirmation for booking #${ref}`);
      } catch (err) {
        console.error(`Failed to resend confirmation for #${ref}:`, err.message);
        actionsTaken.push(`Failed to resend confirmation for #${ref}`);
      }
    } else {
      actionsTaken.push(`Could not find booking #${ref} to resend confirmation`);
    }
  }

  // FLAG_CANCELLATION
  const cancelMatches = aiResponse.matchAll(/\[ACTION:FLAG_CANCELLATION:([^\]]+)\]/g);
  for (const m of cancelMatches) {
    const ref = m[1].trim().replace(/^#/, '');
    const booking = byRef[ref] || byRef[`#${ref}`];
    if (booking) {
      try {
        await updateOne('bookings', { id: booking.id }, {
          $set: {
            status: 'cancelled',
            cancellation_requested: true,
            cancellation_requested_at: new Date().toISOString(),
            cancellation_requested_by: senderEmail,
            cancelledAt: new Date().toISOString(),
            cancelledVia: 'email_ai_support',
          },
        });
        actionsTaken.push(`Cancelled booking #${ref} and notified admin`);
        // SMS to admin via Twilio would go here if Twilio was in the Vercel API
        // For now, admin gets the email notification below
      } catch (err) {
        console.error(`Failed to cancel booking #${ref}:`, err.message);
        actionsTaken.push(`Failed to cancel booking #${ref}`);
      }
    } else {
      actionsTaken.push(`Could not find booking #${ref} to cancel`);
    }
  }

  // FLAG_MODIFICATION
  const modMatches = aiResponse.matchAll(/\[ACTION:FLAG_MODIFICATION:([^:\]]+):([^\]]+)\]/g);
  for (const m of modMatches) {
    const ref = m[1].trim().replace(/^#/, '');
    const details = m[2].trim();
    const booking = byRef[ref] || byRef[`#${ref}`];
    if (booking) {
      try {
        await updateOne('bookings', { id: booking.id }, {
          $set: {
            modification_requested: details,
            modification_requested_at: new Date().toISOString(),
            modification_requested_by: senderEmail,
          },
        });
        actionsTaken.push(`Flagged booking #${ref} for modification: ${details}`);
      } catch (err) {
        console.error(`Failed to flag modification for #${ref}:`, err.message);
        actionsTaken.push(`Failed to flag modification for #${ref}`);
      }
    } else {
      actionsTaken.push(`Could not find booking #${ref} to flag modification`);
    }
  }

  // URGENT_ESCALATE
  if (aiResponse.includes('[ACTION:URGENT_ESCALATE]')) {
    actionsTaken.push('Flagged as urgent — admin notified via email');
  }

  // Strip action tags from customer-facing response
  let cleaned = aiResponse.replace(/\[ACTION:[^\]]*\]/g, '').trim();
  cleaned = cleaned.replace(/\n{3,}/g, '\n\n');

  return { actionsTaken, cleanedResponse: cleaned };
}

/**
 * Look up customer bookings by email address
 */
async function lookupBookings(email) {
  const sql = require('../_lib/db').getDb();
  const todayStr = new Date().toISOString().slice(0, 10);

  try {
    // Upcoming bookings (nearest first)
    const upcoming = await sql(
      `SELECT data FROM bookings WHERE LOWER(data->>'email') = LOWER($1) AND data->>'date' >= $2 ORDER BY data->>'date' ASC LIMIT 5`,
      [email, todayStr]
    );

    // Recent past bookings
    const past = await sql(
      `SELECT data FROM bookings WHERE LOWER(data->>'email') = LOWER($1) AND data->>'date' < $2 ORDER BY data->>'date' DESC LIMIT 3`,
      [email, todayStr]
    );

    return {
      upcoming: upcoming.map(r => r.data),
      past: past.map(r => r.data),
    };
  } catch (err) {
    console.error('Booking lookup error:', err.message);
    return { upcoming: [], past: [] };
  }
}

/**
 * Build booking context string for Claude
 */
function buildBookingContext(upcoming, past) {
  if (upcoming.length === 0 && past.length === 0) {
    return '\n\nNo bookings found for this email address.';
  }

  let ctx = `\n\nThis customer has ${upcoming.length} upcoming and ${past.length} past booking(s):\n`;
  if (upcoming.length > 0) {
    ctx += 'UPCOMING BOOKINGS (most likely what they are asking about):\n';
    for (const b of upcoming) ctx += formatBookingContext(b);
  }
  if (past.length > 0) {
    ctx += 'RECENT PAST BOOKINGS:\n';
    for (const b of past) ctx += formatBookingContext(b);
  }
  return ctx;
}

