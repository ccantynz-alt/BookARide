/**
 * Parse multipart/form-data from Mailgun inbound webhook.
 * Mailgun sends multipart — Vercel's default parser does NOT handle this.
 */
function parseMultipart(req) {
  return new Promise((resolve, reject) => {
    const fields = {};
    const busboy = Busboy({
      headers: req.headers,
      limits: { fileSize: 0 }, // ignore file attachments
    });

    busboy.on('field', (name, value) => {
      fields[name] = value;
    });

    busboy.on('finish', () => resolve(fields));
    busboy.on('error', reject);

    // Vercel may have already parsed the body as a Buffer or string
    if (req.body && Buffer.isBuffer(req.body)) {
      busboy.end(req.body);
    } else if (typeof req.body === 'string') {
      busboy.end(Buffer.from(req.body));
    } else {
      // Stream from raw request
      req.pipe(busboy);
    }
  });
}

