async function handler(req, res) {
  // GET — Mailgun webhook verification
  if (req.method === 'GET') {
    return res.status(200).json({ status: 'ok', message: 'BookaRide email webhook is active' });
  }

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ detail: 'Method not allowed' });

  try {
    // Parse the incoming data — Mailgun sends multipart/form-data
    let fields;
    const contentType = (req.headers['content-type'] || '').toLowerCase();
    if (contentType.includes('multipart/form-data')) {
      fields = await parseMultipart(req);
    } else if (contentType.includes('application/x-www-form-urlencoded') || contentType.includes('application/json')) {
      // Vercel may have parsed it already, or it could be URL-encoded
      fields = req.body || {};
    } else {
      // Try to parse as multipart anyway (Mailgun default)
      try {
        fields = await parseMultipart(req);
      } catch {
        fields = req.body || {};
      }
    }

    const sender = fields.sender || '';
    const fromField = fields.from || sender;
    const subject = fields.subject || 'No Subject';
    const bodyPlain = fields['body-plain'] || '';
    const bodyHtml = fields['body-html'] || '';

    // Extract email address from "Name <email>" format
    const emailMatch = fromField.match(/[\w.\-+]+@[\w.\-]+/);
    const replyToEmail = emailMatch ? emailMatch[0] : fromField;

    // Extract sender name
    const nameMatch = fromField.match(/^([^<]+)/);
    const senderName = nameMatch ? nameMatch[1].trim() : '';

    console.log(`Incoming support email from: ${replyToEmail}, Subject: ${subject}`);

    // Don't reply to our own emails or system addresses
    if (SKIP_PATTERNS.some(p => replyToEmail.toLowerCase().includes(p))) {
      console.log('Skipping auto-reply to system/no-reply email');
      return res.status(200).json({ status: 'skipped', reason: 'system email' });
    }

    // Get email body
    const emailContent = bodyPlain || bodyHtml;
    if (!emailContent) {
      console.log('Empty email body received');
      return res.status(200).json({ status: 'skipped', reason: 'empty body' });
    }

    // Rate limit: max 5 AI replies per sender per day
    const todayStart = new Date();
    todayStart.setUTCHours(0, 0, 0, 0);
    try {
      const sql = require('../_lib/db').getDb();
      const countResult = await sql(
        `SELECT COUNT(*) as cnt FROM email_logs WHERE LOWER(data->>'from') = LOWER($1) AND created_at >= $2`,
        [replyToEmail, todayStart.toISOString()]
      );
      const todayCount = parseInt(countResult[0]?.cnt || '0', 10);
      if (todayCount >= 5) {
        console.log(`Rate limit hit: ${replyToEmail} has ${todayCount} replies today`);
        return res.status(200).json({ status: 'skipped', reason: 'rate limited' });
      }
    } catch (err) {
      console.error('Rate limit check failed (continuing):', err.message);
    }

    // Look up customer bookings
    const { upcoming, past } = await lookupBookings(replyToEmail);
    const allBookings = [...upcoming, ...past];
    const bookingContext = buildBookingContext(upcoming, past);

    // Generate AI response
    const userMessage = `Customer email received:\n\nFrom: ${senderName || 'Customer'} <${replyToEmail}>\nSubject: ${subject}\n\nMessage:\n${emailContent.slice(0, 3000)}${bookingContext}\n\nPlease write a helpful reply to this customer.`;

    let aiResponse = await callClaude(SYSTEM_PROMPT, userMessage);
    if (!aiResponse) {
      aiResponse = staticFallback(senderName || 'there');
    }

    // Process action tags
    const { actionsTaken, cleanedResponse } = await processActions(
      aiResponse, replyToEmail, senderName || 'Customer', allBookings
    );

    if (actionsTaken.length > 0) {
      console.log(`AI agent took ${actionsTaken.length} action(s) for ${replyToEmail}:`, actionsTaken);
    }

    // Build and send reply
    const replySubject = subject.startsWith('Re:') ? subject : `Re: ${subject}`;
    const htmlReply = buildReplyHtml(cleanedResponse);

    const sent = await sendEmail({
      to: replyToEmail,
      subject: replySubject,
      html: htmlReply,
      fromName: 'BookaRide Support',
      from: 'support@bookaride.co.nz',
      replyTo: 'support@bookaride.co.nz',
    });

    if (sent) {
      console.log(`AI support reply sent to ${replyToEmail}`);

      // Log the interaction
      await insertOne('email_logs', {
        id: uuidv4(),
        from: replyToEmail,
        sender_name: senderName,
        subject,
        original_message: emailContent.slice(0, 5000),
        ai_response: aiResponse,
        customer_response: cleanedResponse,
        actions_taken: actionsTaken,
        booking_context: bookingContext.slice(0, 2000),
        status: 'sent',
        created_at: new Date().toISOString(),
      }).catch(err => console.error('Failed to log email interaction:', err.message));

      // Build admin notification
      let actionsHtml = '';
      if (actionsTaken.length > 0) {
        const items = actionsTaken.map(a => `<li>${a}</li>`).join('');
        actionsHtml = `
          <p><strong>Actions taken by AI agent:</strong></p>
          <ul style="background:#fff3cd; padding:15px 15px 15px 35px; border-left:3px solid #ffc107; margin:10px 0;">${items}</ul>
        `;
      }

      const isUrgent = actionsTaken.some(a => a.toLowerCase().includes('urgent'));
      const adminSubjectPrefix = isUrgent ? '[URGENT Support Email]' : '[Support Email]';

      const adminEmail = process.env.ADMIN_EMAIL || process.env.BOOKINGS_NOTIFICATION_EMAIL || 'bookings@bookaride.co.nz';
      await sendEmail({
        to: adminEmail,
        subject: `${adminSubjectPrefix} ${subject} \u2014 from ${replyToEmail}`,
        html: `<div style="font-family:Arial,sans-serif; max-width:600px;">
          <h3 style="color:#D4AF37;">New Support Email</h3>
          <p><strong>From:</strong> ${senderName} &lt;${replyToEmail}&gt;</p>
          <p><strong>Subject:</strong> ${subject}</p>
          <hr>
          <p><strong>Customer wrote:</strong></p>
          <div style="background:#f9f9f9; padding:15px; border-left:3px solid #D4AF37; margin:10px 0;">
            ${emailContent.slice(0, 2000).replace(/\n/g, '<br>')}
          </div>
          ${actionsHtml}
          <p><strong>AI replied (sent to customer):</strong></p>
          <div style="background:#f0f7ff; padding:15px; border-left:3px solid #2196F3; margin:10px 0;">
            ${cleanedResponse.replace(/\n/g, '<br>')}
          </div>
          <p style="font-size:12px; color:#999;">Review in admin: Email Logs</p>
        </div>`,
        fromName: 'BookaRide Support Bot',
      }).catch(err => console.error('Failed to send admin notification:', err.message));

      return res.status(200).json({ status: 'success', message: 'AI support response sent', actions_taken: actionsTaken });
    } else {
      console.error(`Failed to send support reply to ${replyToEmail}`);
      return res.status(500).json({ status: 'error', reason: 'Failed to send reply' });
    }
  } catch (err) {
    console.error('Email auto-responder error:', err.message);
    return res.status(500).json({ status: 'error', reason: 'Email auto-responder error' });
  }
}

