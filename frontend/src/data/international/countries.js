export const getHighPriorityCountries = () => 
  internationalCountries.filter(c => c.priority === 1);

export const getMediumPriorityCountries = () => 
  internationalCountries.filter(c => c.priority === 2);

export const getCountriesByRegion = (region) => {
  const regions = {
    'oceania': ['AU', 'FJ', 'NC', 'PF', 'WS', 'TO', 'CK'],
    'asia': ['CN', 'JP', 'KR', 'IN', 'SG', 'MY', 'TH', 'VN', 'PH', 'ID', 'TW', 'HK'],
    'europe': ['GB', 'DE', 'FR', 'IT', 'ES', 'NL', 'CH', 'AT', 'SE', 'NO', 'DK', 'FI', 'IE', 'PT', 'PL', 'CZ', 'RU'],
    'americas': ['US', 'CA', 'BR', 'AR', 'CL', 'MX'],
    'middle-east': ['AE', 'SA', 'QA', 'IL'],
    'africa': ['ZA']
  };
  return internationalCountries.filter(c => regions[region]?.includes(c.code));
};

export const getCountryBySlug = (slug) => 
  internationalCountries.find(c => c.slug === slug);

export const getCountryByCode = (code) => 
  internationalCountries.find(c => c.code === code);
